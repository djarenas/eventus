#
# - Changing mean to median for systolic_bp requires finding the right
#   line in the script. With eventus: change one word in the YAML.
#
# - Adding a fourth descriptor column requires a new aggregation block.
#   With eventus: add two lines to the semantics YAML and one line to
#   the cleaner YAML.
#
# - Column type (numeric vs category) is implicit — nothing stops you
#   from accidentally applying mean to mobility_status.
#   With eventus: the type is declared in semantics and validated
#   against the aggregation rule at construction.
