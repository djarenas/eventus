# This script required ~275 lines to implement feature parity with
# Chapter 4 of the eventus vignette. Key observations:
#
# - No bad input validation beyond basic cleaning. The eventus
#   cleaner validates column names, causality, date floors, and
#   produces a per-row audit trail. This script has none of that.
#
# - No specific errors raised. The pandas UserWarning above is not
#   an exception — execution continues and may produce wrong output
#   silently. eventus raises at construction with a specific,
#   actionable message before any data is touched.
#
# - compute_coverage_stats() iterates over every member in a Python
#   loop — this does not scale to large cohorts.
#
# - Denominator tracking is manual throughout print_summary().
#   One mistake produces wrong statistics silently.
#
# - compute_age_window() required 40 lines including the Feb 29
#   edge case. eventus handles this in one constructor argument.
#
# - No equivalent of EpisodeCoverageSummary — the structured object
#   with validated denominators and named tiers does not exist.
#   The summary is a printout that cannot be stored, compared,
#   or passed downstream.
#
# - No enriched CohortTimeline — computed statistics must be
#   recomputed every time they are needed downstream.
